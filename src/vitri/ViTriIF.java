    private void btnAddNewLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewLocationActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtAddLocationId.getText().equals("")){
            sb.append("Mã Vị Trí Không Được Để Trống\n");
            if (sb.length()>=0) {
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else if(txtAddLocationName.getText().equals("")){
            sb.append("Tên Vị Trí Không Được Để Trống");
            if (sb.length()>=0) {
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else{
            ViTri vt = new ViTri();
            LocalDate today = LocalDate.now();
            vt.setId(txtAddLocationId.getText());
            vt.setTenViTri(txtAddLocationName.getText());
            vt.setNgayTao(Date.valueOf(today));
            viTriDAOImp.add(vt);
            displayViTri();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        }
    }//GEN-LAST:event_btnAddNewLocationActionPerformed

    private void btnLocationSreachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocationSreachActionPerformed
        if (txtLocationSreach.getText().equals("")) {
            displayViTri();
        }
        try {
            DefaultTableModel table = (DefaultTableModel)tblLocation.getModel();
            String sreach = txtLocationSreach.getText();
            TableRowSorter <DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
            tblLocation.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(sreach));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLocationSreachActionPerformed

    private void btnLocationDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocationDeleteActionPerformed
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            int selectedRow = tblLocation.getSelectedRow();
            TableModel model = tblLocation.getModel();
            String id = model.getValueAt(selectedRow, 0).toString();
            viTriDAOImp.remove(id);
            JOptionPane.showMessageDialog(this, "Xóa thành công", "Success", 1);
            displayViTri();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error", "Error", 0);
        }
    }//GEN-LAST:event_btnLocationDeleteActionPerformed

