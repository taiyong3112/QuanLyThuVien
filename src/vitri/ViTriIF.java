    private void displayViTri(){
        List<ViTri> data = viTriDAOImp.toList();
        DefaultTableModel model = (DefaultTableModel)tblLocation.getModel();
        TableRowSorter <DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        tblLocation.setRowSorter(tr);
        tr.setRowFilter(null);
        Calendar c = Calendar.getInstance();
        model.setRowCount(0);
        for (ViTri vt : data) {
            c.setTime(vt.getNgayTao());
            c.add(Calendar.DATE, 2);
            model.addRow(new Object[] {vt.getId(), vt.getTenViTri(), sdf.format(c.getTime())});
        }
        tblLocation.setModel(model);
    }
    private void updateViTri(String id, String name){
        StringBuilder sb = new StringBuilder();     
        
        if(id.equals("")||name.equals("")){
            sb.append("Không được để trống!");
            if (sb.length()>=0) {
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else{
            ViTri vtUp = new ViTri();
            vtUp.setId(id);
            vtUp.setTenViTri(name);
            viTriDAOImp.edit(vtUp);
        }
    }
