    public void displayRentBook(){
        List<PhieuMuonSach> pmsdata = phieuMuonSachDAOImp.findBook(Integer.parseInt(lblRentId.getText()));
        DefaultTableModel bmodel = (DefaultTableModel) tblRentedBook.getModel();
        bmodel.setRowCount(0);
        String tinhTrang;
        for (PhieuMuonSach pmsl : pmsdata) {
            if (pmsl.getNgayTra() == null) {
                tinhTrang = "Chưa Trả";
            }else{
                tinhTrang = "Đã Trả";
            }
            bmodel.addRow(new Object[]{pmsl.getIdSach(), pmsl.getTenSach(), pmsl.getButDanh(), pmsl.getTenNXB(), tinhTrang});
        }
        tblRentedBook.setModel(bmodel);
    }
    public void updateRentStatus(int idPhieuMuon, String idSach, Date ngayTra){
        PhieuMuonSach pms = phieuMuonSachDAOImp.findSingle(idPhieuMuon, idSach);
        pms.setNgayTra(ngayTra);
        phieuMuonSachDAOImp.edit(pms);
    }
