    private void btnReturnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnBookActionPerformed
        // TODO add your handling code here:
        TableModel model = tblRentedBook.getModel();
        int selectedRow = tblRentedBook.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(rootPane, "Xin mời chọn sách muốn trả", "Thông Báo", 2);
        } else {
            if (model.getValueAt(selectedRow, 4) == "Đã Trả") {
                JOptionPane.showMessageDialog(rootPane, "Sách đã trả không thể trả lần nữa", "Lỗi", 0);
            } else {
                Sach sa = sachDAOImp.find(model.getValueAt(selectedRow, 0).toString());
                PhieuMuon pm = phieuMuonDAOImp.find(Integer.parseInt(lblRentId.getText()));
                LocalDate today = LocalDate.now();
                rf.txtReturnDate.setText(String.valueOf(today));
                rf.lblBookName.setText(model.getValueAt(selectedRow, 1).toString());
                rf.lblRentId.setText(String.valueOf(pm.getId()));
                rf.lblReaderId.setText(lblReaderId.getText());
                if (pm.getHanTra().before(Date.valueOf(today))) {
                    JOptionPane.showMessageDialog(rootPane, "Sách " + sa.getTenSach() + " đã quá hạn trả sách", "Thông Báo", 2);
                    rf.chbViolation.setSelected(true);
                }
                rf.setVisible(true);
            }

        }

    }//GEN-LAST:event_btnReturnBookActionPerformed

