    private void btnAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUserActionPerformed
        // TODO add your handling code here:
        if (txtUserId.getText().equals("") || txtUserName.getText().equals("") || txtPhone.getText().equals("") || pwdUserPassword.getPassword().equals("") || 
                pwdUserPasswordConfirm.getPassword().equals("") || txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Các trường không được để trống", "Lỗi", 0);
        }else if(cboRole.getSelectedItem().toString().equals("Chọn")){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn role cho người dùng", "Lỗi", 0);
        }else if(!Arrays.equals(pwdUserPassword.getPassword(), pwdUserPasswordConfirm.getPassword())){
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu và Xác nhận mật khẩu phải trùng nhau", "Lỗi", 0);
        }else{
            try {
                NguoiDung nd = new NguoiDung();
                LocalDate today = LocalDate.now();
                nd.setId(txtUserId.getText());
                nd.setTenNguoiDung(txtUserName.getText());
                nd.setEmail(txtEmail.getText());
                nd.setMatKhau(pwdUserPassword.getText());
                nd.setSdt(txtPhone.getText());
                nd.setRole(cboRole.getSelectedItem().toString());
                nd.setNgayTao(Date.valueOf(today));
                nguoiDungDAOImp.add(nd);
                JOptionPane.showMessageDialog(rootPane, "Thêm người dùng mới thành công", "Thông Báo", 1);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Thêm người dùng mới không thành công", "Lỗi", 0);
                e.printStackTrace();
            }
         displayNguoiDung();
        }
    }//GEN-LAST:event_btnAddUserActionPerformed

    private void btnUserUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserUpdateActionPerformed
        // TODO add your handling code here:
        TableModel model = tblUser.getModel();
        int selectedRow = tblUser.getSelectedRow();
        String id = model.getValueAt(selectedRow, 0).toString();
        String name = model.getValueAt(selectedRow, 1).toString();
        String email = model.getValueAt(selectedRow, 2).toString();
        String phone = model.getValueAt(selectedRow, 3).toString();
        String role = model.getValueAt(selectedRow, 4).toString();
        up.txtUserId.setText(id);
        up.txtUserName.setText(name);
        up.txtEmail.setText(email);
        up.txtPhone.setText(phone);
        if (role.equals("Admin")) {
            up.cboRole.setSelectedItem(role);
        }else if(role.equals("Thủ Thư")){
            up.cboRole.setSelectedItem(role);
        }
        up.setVisible(true);
    }//GEN-LAST:event_btnUserUpdateActionPerformed

