    public void displayNguoiDung(){
        List<NguoiDung> data = nguoiDungDAOImp.toList();
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);
        Calendar c = Calendar.getInstance();
        for (NguoiDung nd : data) {
            c.setTime(nd.getNgayTao());
            c.add(Calendar.DATE, 2);
            model.addRow(new Object[]{nd.getId(), nd.getTenNguoiDung(), nd.getEmail(), nd.getSdt(), nd.getRole(), sdf.format(c.getTime())});
        }
        tblUser.setModel(model);
    }
    public void updateNguoiDung(String id, String name, String email, String phone, String role){
        NguoiDung nd = new NguoiDung();
        nd.setId(id);
        nd.setTenNguoiDung(name);
        nd.setEmail(email);
        nd.setSdt(phone);
        nd.setRole(role);
        nguoiDungDAOImp.edit(nd);
    }
