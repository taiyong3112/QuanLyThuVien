    public void displayAuthor(){
        List<TacGia> data = tacGiaDAOImp.toList();
        DefaultTableModel model = (DefaultTableModel) tblAuthor.getModel();
        model.setRowCount(0);
        for (TacGia tg : data) {
            model.addRow(new Object[]{tg.getId(), tg.getButDanh()});
        }
        tblAuthor.setModel(model);
    }
