    private void btnAddNewPublisherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewPublisherActionPerformed
        
        int choice = JOptionPane.showConfirmDialog(this, "Bạn muốn thêm mới không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        
        StringBuilder sb = new StringBuilder();     
        
        if(txtAddPublisherId.getText().equals("")){
            sb.append("Mã Nhà Xuất Bản Không Được Để Trống\n");
            if (sb.length()>=0) {
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else if(txtAddPublisherName.getText().equals("")){
            sb.append("Tên Nhà Xuất Bản Không Được Để Trống");
            if (sb.length()>=0) {
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
        }else{
            NhaXuatBan nxb = new NhaXuatBan();
            LocalDate today = LocalDate.now();
            nxb.setId(txtAddPublisherId.getText());
            nxb.setTenNXB(txtAddPublisherName.getText());
            nxb.setNgayTao(Date.valueOf(today));
            nhaXuatBanDAOImp.add(nxb);
            displayNhaXuatBan();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        }

        
    }//GEN-LAST:event_btnAddNewPublisherActionPerformed

