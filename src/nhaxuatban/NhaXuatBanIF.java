    private void btnPublisherDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPublisherDeleteActionPerformed
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            int selectedRow = tblPublisher.getSelectedRow();
            TableModel model = tblPublisher.getModel();
            String id = model.getValueAt(selectedRow, 0).toString();
            nhaXuatBanDAOImp.remove(id);
            JOptionPane.showMessageDialog(this, "Xóa thành công", "Success", 1);
            displayNhaXuatBan();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error", "Error", 0);
        }
    }//GEN-LAST:event_btnPublisherDeleteActionPerformed

