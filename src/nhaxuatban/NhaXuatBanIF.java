    private void displayNhaXuatBan(){
        List<NhaXuatBan> data = nhaXuatBanDAOImp.toList();
        
        DefaultTableModel model = (DefaultTableModel)tblPublisher.getModel();
        //Xoa bo du ieu cu de addRow ko trung lap
        model.setRowCount(0);
        TableRowSorter <DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        tblPublisher.setRowSorter(tr);
        tr.setRowFilter(null);
        for (NhaXuatBan nxb : data) {
            model.addRow(new Object[] {nxb.getId(), nxb.getTenNXB(), nxb.getNgayTao()});
        }
        tblPublisher.setModel(model);
    }
