    private void btnPublisherSreachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPublisherSreachActionPerformed
        // TODO add your handling code here:
        if (txtPublisherSreach.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cần nhập thông tin để tìm kiếm !");
            return;
        }
        
        try {
            DefaultTableModel table = (DefaultTableModel)tblPublisher.getModel();
            String search = txtPublisherSreach.getText();
            TableRowSorter <DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
            tblPublisher.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(search));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnPublisherSreachActionPerformed

    private void txtAddPublisherIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAddPublisherIdKeyReleased
        // TODO add your handling code here:
        String pattern = "^XB[0-9]{3}$";
        Pattern patt = Pattern.compile(pattern);
        Matcher match = patt.matcher(txtAddPublisherId.getText());
        if(!match.matches()){
            lblIdErr.setText("Mã Nhà Xuất Bản phải có 5 ký tự và theo định dạng XBxxx (x là số)");
        }else{
            lblIdErr.setText("");
        }
    }//GEN-LAST:event_txtAddPublisherIdKeyReleased


